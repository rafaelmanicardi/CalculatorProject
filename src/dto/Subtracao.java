public class Subtracao{
    private double a,b;

    public void setA(double a) {
        this.a = a;
    }
    public double getA() {
        return a;
    }
    public void setB(double b) {
        this.b = b;
    }
    public double getB() {
        return b;
    }

    public double somar(){
        return a/b;
    }
    
}